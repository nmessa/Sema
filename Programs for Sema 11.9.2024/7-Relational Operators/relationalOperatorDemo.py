
#Declare variables and assign values
x = 10
y = 20
z = 30

#Greater than
print(x > 20)   #False

#Greater than or equal to
print(x >= 10)  #True

#Less than
print(y < 30)   #True

#Less than or equal to
print(y <= 20)  #True

#Equal to
print(x == y)   #False

#Not equal to
print(x != z)   #True


