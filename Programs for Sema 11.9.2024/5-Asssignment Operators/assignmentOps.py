number = 10
print("Original number =", number)

number += 5
print("Add 5 =", number)

number -= 10
print("Subtract 10 =", number)

number *= 12
print("Multiply by 12 =", number)

number //= 7
print("Integer divide by 7 =", number)

number %= 7
print("Remainder 7 =", number)

number /= 3
print("Floating point divide by 3 =", number)

